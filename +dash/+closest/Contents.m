%% dash.closest  Functions that locate points closest to specified coordinates
% ----------
%   These functions implement routines that help locate points in space
%   that are closest to a set of coordinates. They are most often used to
%   help users locate the data points in a gridfile that are closest to a
%   set of proxy record locations.
% ----------
% Functions:
%   latlon  - Return the gridded latitude-longitude points closest to coordinates
%   site    - Return the coordinates of sites closest to specified coordinates
%
% Unit tests:
%   tests   - Unit testing for the dash.closest package
%
